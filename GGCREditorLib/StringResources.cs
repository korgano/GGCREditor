        public static void SetLanguage(string languageCode)
        {
            if (_strings == null)
                Initialize();
            _currentLanguage = languageCode;
        }

        public static string Get(string key)
        {
            if (_strings == null)
                Initialize();

            string lang = _currentLanguage;
            if (!_strings.ContainsKey(lang))
                lang = "english";

            if (_strings[lang].ContainsKey(key))
                return _strings[lang][key];

            if (lang != "english" && _strings["english"].ContainsKey(key))
                return _strings["english"][key];

            return key;
        }
